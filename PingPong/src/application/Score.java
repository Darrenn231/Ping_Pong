package application;

public class Score {
	int scoreP1;
	int scoreP2;
	
	
	public Score(int scoreP1, int scoreP2) {
		super();
		this.scoreP1 = scoreP1;
		this.scoreP2 = scoreP2;
	}
	public int getScoreP1() {
		return scoreP1;
	}
	public void setScoreP1(int scoreP1) {
		this.scoreP1 = scoreP1;
	}
	public int getScoreP2() {
		return scoreP2;
	}
	public void setScoreP2(int scoreP2) {
		this.scoreP2 = scoreP2;
	}
	
	
}
